    private void btnpesquisarHistoricoVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpesquisarHistoricoVendasActionPerformed
        // botao buscar venda por periodo
        try {
            //Receber as datas
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");

            LocalDate data_inicio = LocalDate.parse(txtdatainicioHistoricoVendas.getText(), formato);
            LocalDate data_fim = LocalDate.parse(txtdatafimHistoricoVendas.getText(), formato);

            VendasDAO dao = new VendasDAO();
            List<Vendas> lista = dao.listarVendasPorPeriodo(data_inicio, data_fim);

            DefaultTableModel dados = (DefaultTableModel) tabelaHistorico.getModel();
            dados.setNumRows(0);

            for (Vendas v : lista) {
                dados.addRow(new Object[]{
                    v.getId(),
                    v.getData_venda(),
                    v.getCliente().getNome(),
                    v.getTotal_venda(),
                    v.getObs(),
                    v.getData_vencimento(),
                    v.getData_pgto()
                });

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Digite duas datas como intervalo!");
        }
    }//GEN-LAST:event_btnpesquisarHistoricoVendasActionPerformed

