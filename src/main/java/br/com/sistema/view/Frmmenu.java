    private void txtcepClientesKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcepClientesKeyPressed

        //Programacao do keypress
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Clientes obj = new Clientes();
            ClientesDAO dao = new ClientesDAO();
            obj = dao.buscaCep(txtcepClientes.getText());

            if (obj != null) {
                txtendClientes.setText(obj.getEndereco());
                txtbairroClientes.setText(obj.getBairro());
                txtcidadeClientes.setText(obj.getCidade());
                cbufClientes.setSelectedItem(obj.getUf());
            }
        }
    }//GEN-LAST:event_txtcepClientesKeyPressed

