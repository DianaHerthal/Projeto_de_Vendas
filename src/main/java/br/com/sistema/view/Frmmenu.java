    private void btnbuscaClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscaClientesActionPerformed
        // botao buscar cliente por nome

        String nome = txtnomeClientes.getText();
        Clientes obj = new Clientes();
        ClientesDAO dao = new ClientesDAO();

        obj = dao.consultaPorNome(nome);

        if (obj.getNome() != null) {

            //Exibi os dados do obj nos campos de texto
            txtcodigoClientes.setText(String.valueOf(obj.getId()));
            txtnomeClientes.setText(obj.getNome());
            txtrgClientes.setText(obj.getRg());
            txtcpfClientes.setText(obj.getCpf());
            txtemailClientes.setText(obj.getEmail());
            txtfixoClientes.setText(obj.getTelefone());
            txtcelClientes.setText(obj.getCelular());
            txtcepClientes.setText(obj.getCep());
            txtendClientes.setText(obj.getEndereco());
            txtnumeroClientes.setText(String.valueOf(obj.getNumero()));
            txtcomplementoClientes.setText(obj.getComplemento());
            txtbairroClientes.setText(obj.getBairro());
            txtcidadeClientes.setText(obj.getCidade());
            cbufClientes.setSelectedItem(obj.getUf());
        } else {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
        }
    }//GEN-LAST:event_btnbuscaClientesActionPerformed

